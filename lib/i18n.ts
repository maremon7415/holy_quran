type Language = keyof typeof translations
type Key = keyof typeof translations.en

const localeMap: Record<Language, string> = {
  en: 'en-US',
  bn: 'bn-BD',
  ar: 'ar',
}

function formatTemplate(template: string, values?: Record<string, string | number>) {
  if (!values) {
    return template
  }
  return Object.entries(values).reduce(
    (text, [key, value]) => text.replaceAll(`{${key}}`, String(value)),
    template
  )
}

export function useI18n() {
  const { language } = useAppStore()
  const current = translations[language] || translations.en

  const t = (key: Key, values?: Record<string, string | number>) => {
    const text = current[key] || translations.en[key] || key
    return formatTemplate(text, values)
  }

  return {
    language,
    t,
    locale: localeMap[language] || localeMap.en,
    isRTL: language === 'ar',
  }
}
